                command.CommandText = "select * from [Workers] where login=@login ";
                command.Parameters.Add("@login", SqlDbType.NVarChar).Value = username;
                using (var reader = command.ExecuteReader())
                {
                    if (reader.Read())
                    {
                        user = new UserModel()
                        {
                            id = reader[0].ToString(),
